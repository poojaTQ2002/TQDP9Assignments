package com.basic;

public class RelationalOperators {
	public static void main(String[] args) {
		int a=12;
		int b=24;
		int c=12;
		System.out.println(a<b && b>=a);
		System.out.println(a>b || b<a);
		System.out.println(a==c || c<=b);
		System.out.println(c!=b && a==c);
		System.out.println(!(a==c));
		
		
		
		
		
	}

}
