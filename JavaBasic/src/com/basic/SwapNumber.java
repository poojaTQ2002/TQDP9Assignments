package com.basic;

public class SwapNumber {
	public static void main(String args[])
	{
		int num1,num2,temp;
		num1=10;
		num2=20;
		System.out.println("Before swap number :"+"num1="+num1+" num2="+num2);
		temp=num1;
		num1=num2;
		num2=temp;
		System.out.println("After swap number :"+"num1="+num1+" num2="+num2);
	}

}
