package com.basic;

public class Casting {
	public static void main(String[] args) {
		//1
		int num=10;
		float f=num;
		System.out.println("float is..."+f);
		//2
		int num2=55;
		char ch2=(char) num2;
		System.out.println("charactor is..."+ch2);
		//3
		double salary=25466.245;
		int a=(int) salary;
		System.out.println("salary is..."+a);
		//4
		float percentage=25.44f;
		int z=(int) percentage;
		System.out.println("percentage is..."+z);
		//5
		long x=1234569741;
		short p=(short) x;
		System.out.println("p is..."+x);
		
		
	}

}
