package com.basic;

public class ConvertLength {
	public static void main(String args[])
	{
		float length,meter,kilometer,centimeter;
		length=100;
		System.out.println("Length is :"+length);
		meter=length/100;
		System.out.println("meter is :"+meter);
		kilometer=length/1000;
		System.out.println("kilometer is :"+kilometer);
		centimeter=length/10;
		System.out.println("centimeter is :"+centimeter);
		
		
	}
}
