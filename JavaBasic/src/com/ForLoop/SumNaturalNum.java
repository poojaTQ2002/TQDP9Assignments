package com.ForLoop;

public class SumNaturalNum {
	public static long naturalnum()
	{
		{
			int sum=0;
			for(int i=1;i<=10;i++)
			{
				sum+=i;
				
			}
			return sum;
		}	
	
	}
	public static void main(String[] args) {
		System.out.println("Sum of First 10 Natural Number is :"+naturalnum());
	}
		
		
	}


